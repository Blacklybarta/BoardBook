package fr.eni.ecole.boardbook.bo;

public class Point <X, Y, Z>{
	private X x;
	private Y y;
	private Z z;
	
	public Point (){
		
	}

	public X getX() {
		return x;
	}

	public void setX(X x) {
		this.x = x;
	}

	public Y getY() {
		return y;
	}

	public void setY(Y y) {
		this.y = y;
	}

	public Z getZ() {
		return z;
	}

	public void setZ(Z z) {
		this.z = z;
	}
	
	
}
